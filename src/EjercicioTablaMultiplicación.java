public class EjercicioTablaMultiplicación {
    public static void main(String[] args) {
        for(int contador = 0; contador <= 10; contador++){
            for(int multiplicación = 0; multiplicación <= 10; multiplicación++){
                System.out.println(contador*multiplicación);
                System.out.println("");
            }
            System.out.println();
        }
    }
}
