public class operacionesentrenumeros {
    public static void main(String[] args) {
        double peso = 4.0;
        int cantidad = 10;
        System.out.println(peso * cantidad);

        double precio = 5.5;
        int tickets = 4;
        System.out.println(precio * tickets);

        int regalos = 14;
        int personas = 2;
        System.out.println(regalos / personas);

        int dia = 4;
        int otroDia = 4 + dia;
        System.out.println(otroDia);
    }
}
